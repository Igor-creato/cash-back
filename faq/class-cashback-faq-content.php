    /**
     * Список валидных slug'ов категорий.
     *
     * @return array<int, string>
     */
    public static function category_slugs(): array {
        return array_keys(self::get_categories());
    }

    /**
     * Получить одну категорию по slug. Возвращает null если slug неизвестен.
     *
     * @return array{title: string, items: array<int, array{q: string, a: string}>}|null
     */
    public static function get_category( string $slug ): ?array {
        $all = self::get_categories();
        return $all[ $slug ] ?? null;
    }
